                    <span style="color: #008000;">N</span>
                  </h2>
                  <p style="padding-left: 10px;">
                    <strong>NOT</strong> Negace
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT</span> *
                    <span style="color: #99ccff;">FROM</span> tabulka
                    <span style="color: #99ccff;">WHERE</span> sloupec
                    <span style="color: #99ccff;">NOT IN</span> (2012);
                  </p>
                  <p style="padding-left: 10px;">
                    <strong>NULL</strong> Nulová hodnota
                  </p>&nbsp;
                  <h2>
                    <span style="color: #008000;">O</span>
                  </h2>
                  <p style="padding-left: 10px;">
                    <strong>OR</strong> Podmínka NEBO (buď, anebo)
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT</span> *
                    <span style="color: #99ccff;">FROM</span> tabulka
                    <span style="color: #99ccff;">WHERE</span> sloupec = 'toto'
                    <span style="color: #99ccff;">OR</span> sloupec = 'tamto';
                  </p>
                  <p style="padding-left: 10px;">
                    <strong>ORDER BY</strong> Řadí hodnoty ve sloupci vzestupně. Používá se společně
                    s
                    ASC/DESC
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT</span> sloupec
                    <span style="color: #99ccff;">FROM</span> tabulka
                    <span style="color: #99ccff;">ORDER BY</span> sloupec;
                  </p>&nbsp;
                  <h2>
                    <span style="color: #008000;">R</span>
                  </h2>
                  <p style="padding-left: 10px;">
                    <strong>RIGHT</strong> Vypíše znaky z řetězce začínající zprava
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT RIGHT</span>('text', 3)
                    <span style="color: #99ccff;">FROM</span> tabulka;
                  </p>
                  <p style="padding-left: 10px;">
                    <strong>ROUND</strong> Zaokrouhluje čísla na desetinná místa
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT ROUND</span>
                    (3.77, 2);
                  </p>&nbsp;
                  <h2>
                    <span style="color: #008000;">S</span>
                  </h2>
                  <p style="padding-left: 10px;">
                    <strong>SPLIT</strong> Rozděluje text podle oddělovače
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT SPLIT</span>(sloupec, ' ')
                    <span style="color: #99ccff;">FROM</span> tabulka;
                  </p>
                  <p style="padding-left: 10px;">
                    <strong>SUBSTRING</strong> Vypisuje část řetězce od dané pozice
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT SUBSTRING</span>('text', 3);
                  </p>
                  <p style="padding-left: 10px;">
                    <strong>SUM</strong> Součet
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT SUM</span>(sloupec)
                    <span style="color: #99ccff;">FROM</span> tabulka;
                  </p>&nbsp;
                  <h2>
                    <span style="color: #008000;">T</span>
                  </h2>
                  <p style="padding-left: 10px;">
                    <strong>TIMESTAMP</strong> Vypíše datum s časem
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT TIMESTAMP</span>('2019-09-14');
                  </p>
                  <p style="padding-left: 10px;">
                    <strong>TO_DATE</strong> Převede na datum
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT TO_DATE</span>('09/14/2019');
                  </p>
                  <p style="padding-left: 10px;">
                    <strong>TRIM</strong> Odstraní mezery
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT TRIM</span>(' bla ');
                  </p>&nbsp;
                  <h2>
                    <span style="color: #008000;">U</span>
                  </h2>
                  <p style="padding-left: 10px;">
                    <strong>UPPER</strong> Převádí písmena na VELKÁ
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT UPPER</span>('malá písmena');
                  </p>
                  <p style="padding-left: 10px;">
                    <strong>UNION</strong> Spojí výběr ze dvou tabulek ("požadavky" musí být shodné)
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT</span> *
                    <span style="color: #99ccff;">FROM</span> tabulka1
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">UNION</span>
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT</span> *
                    <span style="color: #99ccff;">FROM</span> tabulka2;
                  </p>
                  <p style="padding-left: 10px;">
                    <strong>UNION ALL</strong> Spojí výběr i s opakujícími se hodnotami
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT</span> *
                    <span style="color: #99ccff;">FROM</span> tabulka1
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">UNION ALL</span>
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">SELECT</span> *
                    <span style="color: #99ccff;">FROM</span> tabulka2;
                  </p>
                  <p style="padding-left: 10px;">
                    <strong>UPDATE</strong> Aktualizuje (např. data v tabulce)
                  </p>
                  <p style="padding-left: 30px;">
                    <span style="color: #99ccff;">UPDATE</span> tabulka;
                  </p>&nbsp;
                  <h2>
                    <span style="color: #008000;">W</span>
                  </h2>
                  <p style="padding-left: 10px;">
                    <strong>WHEN</strong> Podmínka když
                  </p>
                  <p style="padding-left: 10px;">
                    <strong>WHERE</strong>
